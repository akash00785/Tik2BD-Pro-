def get_ad_link():
    """
    বিজ্ঞাপন_লিংক.txt ফাইল থেকে সরাসরি লিংক পড়ে (প্রতিবার নতুন করে) —
    তাই ফাইলে লিংক বসিয়ে সেভ করার সাথে সাথেই সাইটে সেটা কার্যকর হয়ে
    যায়, সার্ভার রিস্টার্ট করার দরকার হয় না।
    """
    try:
        with open(AD_LINK_FILE, 'r', encoding='utf-8') as f:
            content = f.read()
    except FileNotFoundError:
        return ''

    for line in content.splitlines():
        line = line.strip()
        if not line or line.startswith('#'):
            continue
        return line
    return ''


def ads_enabled():
    """বিজ্ঞাপন_লিংক.txt ফাঁকা থাকলে অ্যাড-গেট বন্ধ, লিংক থাকলে চালু।"""
    return bool(get_ad_link())


